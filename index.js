//Perform basic math operation like add, subtract, multiply and divide
class MathOperation {
    constructor() {

    }

    add = (num1, num2) => {
        return (num1 + num2)
    }

    sub = (num1, num2) => {
        return (num1 - num2)
    }

    multiply = (num1, num2) => {
        return (num1 * num2)
    }

    divide = (num1, num2) => {
        return (num1 / num2)
    }

}

module.exports = MathOperation